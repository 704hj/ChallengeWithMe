package kr.score.vo;

public class ScoreVO {
	private long us_num;
	private int us_score;
	private int join_num;
	private int complete_num;
	private int create_num;
	private int charge_amount;
	private int spend_amount;
	private int quit_amount;
	
	
	public long getUs_num() {
		return us_num;
	}
	public void setUs_num(long us_num) {
		this.us_num = us_num;
	}
	public int getUs_score() {
		return us_score;
	}
	public void setUs_score(int us_score) {
		this.us_score = us_score;
	}
	public int getJoin_num() {
		return join_num;
	}
	public void setJoin_num(int join_num) {
		this.join_num = join_num;
	}
	public int getComplete_num() {
		return complete_num;
	}
	public void setComplete_num(int complete_num) {
		this.complete_num = complete_num;
	}
	public int getCreate_num() {
		return create_num;
	}
	public void setCreate_num(int create_num) {
		this.create_num = create_num;
	}
	public int getCharge_amount() {
		return charge_amount;
	}
	public void setCharge_amount(int charge_amount) {
		this.charge_amount = charge_amount;
	}
	public int getSpend_amount() {
		return spend_amount;
	}
	public void setSpend_amount(int spend_amount) {
		this.spend_amount = spend_amount;
	}
	public int getQuit_amount() {
		return quit_amount;
	}
	public void setQuit_amount(int quit_amount) {
		this.quit_amount = quit_amount;
	}
	
	
	
}
